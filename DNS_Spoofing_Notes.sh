# What is a DNS Server?
# A DNS Server acts as a telephone book. A web address
# needs to be translated into an IP address. The IP
# address identifies where the web site requested by the
# user is located. A DNS Server contains a list of web
# addresses and their corresponding IP addresses.
# DNS Servers provide a welcome alternative to
# memorizing IP addresses.

# Frequently visited sites are saved locally for
# convenience. A DNS database, or cache, resides on
# your computer and contains information on sites
# you visit commonly.

